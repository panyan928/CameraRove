/*+ FUNCTION DESCRIPTION ----------------------------------------------
  NAME: mth_acos_degree
  DESCRIPTION:
    The function shall compute an arc cosine in degrees. 
	{
    The function result shall be equivalent to acos(abs(par_f_deltax/par_f_radius)) with a precision of 1.0e-1. 
    The normal range of par_f_deltax is [-par_f_radius,par_f_radius]. 
    The normal range of par_f_radius is ]MTH_MIN_F, MTH_FLOAT_MAX]. 
    If par_f_deltax is in range [-MTH_FLOAT_MAX,-par_f_radius] or [par_f_radius,MTH_FLOAT_MAX], the returned value shall be 0.0 degree. 
    If par_f_radius is in range [-MTH_FLOAT_MAX, MTH_MIN_F], the returned value shall be 90.0 degrees. 
    }
    MTH_MIN_F is 0.001.
    MTH_FLOAT_MAX is 3.402823466e+38.
    Algorithm: 
    The computation shall be done through an interpolation table containing each resulting angle. 
     The index of the table shall be a rule of three between the size of the table, the given radius par_f_radius 
    and the given difference between x coordinates par_f_deltax. 
  SCOPE: global
  PARAMETERS:
    par_f_deltax -> the difference between the two x coordinates of the points determining the angle with the horizontal.
    par_f_radius -> distance between the two points defining the angle with the horizontal. 
  RETURN:
    SGLfloat -> value of the arc cosine in the range [0.0F, 90.0] degrees  
---------------------------------------------------------------------+*/
SGLfloat mth_acos_degree(SGLfloat par_f_deltax, SGLfloat par_f_radius)
{
    SGLfloat loc_f_result;
    SGLlong loc_l_n;
#ifdef ATTOL_CRC_PRAGMA
#pragma attol crc_justify_all (Rule M12.6.2, 2, "Rule M12.6.2 is not applied here as a macro is used")
#endif
    if (mth_f_abs(par_f_deltax) > par_f_radius) {
        loc_f_result = 0.0F;
    }
    else {
        if (par_f_radius > MTH_MIN_F) {
            loc_l_n = mth_f_to_l((par_f_deltax * MTH_ACOS_TABLE_LENGTH) / par_f_radius);
            if (loc_l_n > (SGLlong) MTH_MAX_HIGH_ACOS_ENTRY) {
#ifdef ATTOL_CRC_PRAGMA
#pragma attol crc_justify_all (Rule M12.6.2, 4, "Rule M12.6.2 is not applied here as a macro is used")
#pragma attol crc_justify_all (Rule M10.4, 3, "Rule M10.4 is not applied here as the cast of a float to an integer is the purpose of the macro.")
#endif
                loc_l_n = (SGLlong) (mth_f_to_ul((par_f_deltax * MTH_ACOS_HIGH_TABLE_SIZE) / par_f_radius));
                loc_l_n = loc_l_n - ((SGLlong) ((SGLulong) MTH_ACOS_HIGH_TABLE_SIZE - (SGLulong) MTH_ACOS_LOW_TABLE_LENGTH));
                if (loc_l_n < 0) {
                    loc_l_n = 0;
                }
                else {
                    /* Nothing to do */
                }
                loc_f_result = mth_tf_acos_low_table[loc_l_n];
            }
            else {
                loc_f_result = mth_tf_acos_table[loc_l_n];
            }
        }
        else {
            loc_f_result = mth_tf_acos_table[0L];
        }
    }

    return loc_f_result;
}

